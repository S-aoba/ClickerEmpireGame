export class Items {
	constructor(name, type, currentAmount, maxAmount, perMoney, perRate, price, url) {
		this.name = name;
		this.type = type;
		this.currentAmount = currentAmount;
		this.maxAmount = maxAmount;
		this.perMoney = perMoney;
		this.perRate = perRate;
		this.price = price;
		this.url = url;
	}
}